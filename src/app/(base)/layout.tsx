import '../globals.css'
import type { Metadata } from 'next'
import { Inter } from 'next/font/google'

const inter = Inter({ subsets: ['latin'] })
import { RightSidebar, LeftSidebar, BottomNav } from '@/components/common'
import { sataoshi } from '../font-config'
import { ThemeProvider } from '@/components/theme-provider'
import SessionProvider from '@/components/session-provider'
import { getServerSession } from 'next-auth'
import { redirect } from 'next/navigation'
import { TopNav } from '@/components/common'
import Footer from '@/components/common/footer'
export const metadata: Metadata = {
  title: 'Create Next App',
  description: 'Generated by create next app'
}

export default async function RootLayout({ children }: { children: React.ReactNode }) {
  const session = await getServerSession()
  if (!session || !session.user) {
    redirect('/sign-in')
  }
  return (
    <html lang='en'>
      <SessionProvider session={session}>
        <ThemeProvider attribute='class' defaultTheme='dark' enableSystem>
          <body className={sataoshi.className}>
            <main className='flex justify-between'>
              <LeftSidebar />
              <section className='flex-1 flex justify-center ' id='base-container'>
                <TopNav />
                <div className='w-full max-w-2xl pt-24 pb-40'>{children}</div>
              </section>
              <RightSidebar />
            </main>
            <Footer />
          </body>
        </ThemeProvider>
      </SessionProvider>
    </html>
  )
}
