import '../globals.css'

import { RightSidebar, LeftSidebar, BottomNav } from '@/components/common'
import { TopNav } from '@/components/common'
import Footer from '@/components/common/footer'
import SessionProvider from '@/components/session-provider'
import { ThemeProvider } from '@/components/theme-provider'
import { Toaster } from '@/components/ui/toaster'
import { getSingleUser } from '@/lib/actions/user-action'
import { authOptions } from '@/lib/auth'
import type { Metadata } from 'next'
import { redirect } from 'next/navigation'
import { getServerSession } from 'next-auth'

import { sataoshi } from '../font-config'

export const metadata: Metadata = {
  title: 'Create Next App',
  description: 'Generated by create next app'
}

export default async function RootLayout({ children }: { children: React.ReactNode }) {
  const session = await getServerSession(authOptions)

  console.log(session)
  if (!session || !session.user) {
    redirect('/sign-in')
  }
  const { data } = await getSingleUser(session!.user.id)
  console.log('layout data --> ', data)
  if (!data?.isOnboard) redirect('/onboarding')

  return (
    <html lang='en'>
      <SessionProvider session={session}>
        <ThemeProvider attribute='class' defaultTheme='dark' enableSystem>
          <body className={sataoshi.className}>
            <main className='flex justify-between'>
              <LeftSidebar userId={data._id} username={data.username} sessionId={session.user.id} />
              <section className='flex-1 flex justify-center px-4' id='base-container'>
                <TopNav />
                <div className='w-full max-w-2xl pb-40 max-md:pt-24 pt-32'>{children}</div>
              </section>
              <RightSidebar />
            </main>
            <Footer />
            <BottomNav />
            <Toaster />
          </body>
        </ThemeProvider>
      </SessionProvider>
    </html>
  )
}
